        <section>
            <button class="accordion">Can I use a personal music device while running?</button>

            <p class="panel">
                During any running segments, we prefer for athletes to NOT use music devices to maximize safety, assure a 
                fair competitive environment and assure athletes can hear all course marshal instructions. With that said, 
                we also understand that many athletes rely on music to help endure the challenge of running. To accommodate 
                the needs of our athletes while still assuring maximum safety and a fair, competitive environment, here is 
                the arrangement that we can accommodate.<br><br>

                If you chose to use a personal music device, you must always keep one ear open for instructions so only one 
                ear-bud can be used at any time. In addition, if you opt to use music, you will not be eligible for awards, 
                points and rankings. So as an athlete, you have to make a choice…music or awards/points/rankings.<br><br>

                If you are using a personal music devise with two earbuds, you will be immediately disqualified. Trust us, 
                we don’t want to have to enforce a disqualification but in order to assure the safety of all athletes, 
                this is a very important rule.<br><br>

                We also request that if you chose to use a personal music device that you keep the volume to a minimum to 
                assure all instructions can be heard.
            </p>
        </section>

